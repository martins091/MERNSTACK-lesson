// Variables
// Variables are containers for storing data. You can declare variables using var, let, or const.
// In JavaScript, a variable is a container for storing data values. You can think of variables as named 
// storage locations that can hold different types of data.

// Declaring Variables
// You can declare variables using three keywords:

// var: The oldest way to declare variables. It has function scope or global scope.
// let: Introduced in ES6 (ES2015), it allows you to declare block-scoped variables. 
//     It's preferred for most use cases.
// const: Also introduced in ES6, it allows you to declare block-scoped constants. Once assigned, 
//     their values cannot be changed.

// Using var
var name = "John"; // function-scoped or globally scoped

// Using let
let age = 30; // block-scoped

// Using const
const pi = 3.14; // block-scoped and cannot be reassigned


