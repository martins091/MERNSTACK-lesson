// JavaScript is a programming language that enables interactive web pages. 
// It is an essential part of web applications and can be used both on the client-side 
// (in the browser) and server-side (on the server).

// Including JavaScript in HTML
// You can include JavaScript in an HTML file in three ways:

// Inline: Using the onclick attribute in HTML tags.
// Internal: Using a <script> tag in the HTML <head> or <body>.
// External: Linking to an external JavaScript file using the <script src="filename.js"></script> tag.

// <!DOCTYPE html>
// <html>
// <head>
//     <title>JavaScript Example</title>
//     <script src="script.js"></script> <!-- External Script -->
// </head>
// <body>
//     <h1>Hello, JavaScript!</h1>
//     <script>
//         // Internal Script
//         alert("Welcome to JavaScript!");
//     </script>
// </body>
// </html>
