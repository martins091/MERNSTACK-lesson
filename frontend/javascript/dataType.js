// Data Types
// JavaScript has several built-in data types. They can be divided into two categories:

// Primitive and Reference types.

// Primitive Data Types
// String: Represents text. Strings are enclosed in quotes (single or double).
// Example: let greeting = "Hello, World!";

// Number: Represents both integer and floating-point numbers.
// Example: let score = 95; or let price = 12.99;

// Boolean: Represents a logical entity that can be either true or false.
// Example: let isLoggedIn = true;

// Undefined: A variable that has been declared but not assigned a value is undefined.
// Example: let notAssigned; // notAssigned is undefined

// Null: Represents the intentional absence of any object value. It is an assignment value.
// Example: let emptyValue = null;

// Symbol: A unique and immutable primitive value that can be used as the key of an object property (ES6).
// Example: let uniqueKey = Symbol("key");

// BigInt: A numeric data type that can represent integers with arbitrary precision (ES11).
// Example: let bigNumber = 1234567890123456789012345678901234567890n;


// Reference Data Types

// Object: A collection of properties, which can include primitive values, other objects, or functions.
let person = {
    name: "Alice",
    age: 25,
    isStudent: false
  };

//   Array: A special type of object used to store ordered collections of values.
let fruits = ["Apple", "Banana", "Cherry"];
